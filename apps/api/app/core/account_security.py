def create_account(connection: Any, username: str, email: str, password: str) -> dict[str, str]:
    ensure_account_security_schema(connection)
    username = str(username or "").strip()
    if not username:
        raise ValueError("Kullanıcı adını girin.")
    email = validate_signup_email(email)
    validate_password_policy(password)
    cursor = connection.cursor(dictionary=True)
    cursor.execute("SELECT id FROM kullanicilar WHERE LOWER(kullanici_adi) = LOWER(%s) LIMIT 1", (username,))
    if cursor.fetchone():
        raise ValueError("Bu kullanıcı adı zaten kullanılıyor.")
    cursor.execute("SELECT id FROM kullanicilar WHERE LOWER(email) = LOWER(%s) LIMIT 1", (email,))
    if cursor.fetchone():
        raise ValueError("Bu e-posta adresi zaten kullanılıyor.")
    role = None
    for role_name in SIGNUP_DEFAULT_ROLE_NAMES:
        cursor.execute("SELECT id FROM roller WHERE LOWER(rol_adi) = LOWER(%s) LIMIT 1", (role_name,))
        role = cursor.fetchone()
        if role:
            break
    if not role:
        raise ValueError("Varsayılan kullanıcı rolü bulunamadı.")
    cursor.execute(
        """
        INSERT INTO kullanicilar (kullanici_adi, email, sifre_hash, rol_id, email_verified, is_active)
        VALUES (%s, %s, %s, %s, FALSE, FALSE)
        """,
        (username, email, hash_password(password), int(role["id"])),
    )
    connection.commit()
    send_verification_email(connection, email)
    return {"status": "created", "message": "Hesabınız oluşturuldu. Giriş yapmadan önce e-posta adresinizi doğrulayın."}


def send_verification_email(connection: Any, email: str) -> dict[str, str]:
    ensure_account_security_schema(connection)
    user = _find_user_by_email(connection, email)
    if not user:
        raise ValueError("Bu e-posta adresi ile eşleşen kullanıcı bulunamadı.")
    if bool(user.get("email_verified")):
        return {"status": "already_verified", "message": "Bu kullanıcının e-postası zaten doğrulanmış."}
    consume_rate_limit(
        connection,
        "verification_send",
        str(user["email"]),
        VERIFICATION_SEND_RATE_LIMIT[0],
        VERIFICATION_SEND_RATE_LIMIT[1],
        "Çok fazla doğrulama kodu talebi alındı.",
    )
    code = _create_numeric_code()
    expires_at = datetime.now() + timedelta(hours=EMAIL_VERIFICATION_EXPIRY_HOURS)
    cursor = connection.cursor()
    cursor.execute("UPDATE email_verification_tokens SET used_at = NOW() WHERE user_id = %s AND used_at IS NULL", (user["id"],))
    cursor.execute(
        "INSERT INTO email_verification_tokens (user_id, token_hash, expires_at) VALUES (%s, %s, %s)",
        (user["id"], _token_hash(code), expires_at),
    )
    connection.commit()
    subject, body, html_body = _verification_email_content(str(user["kullanici_adi"]), code)
    return _send_email(str(user["email"]), subject, body, html_body)


def verify_email_code(connection: Any, email: str, code: str) -> dict[str, str]:
    ensure_account_security_schema(connection)
    email = validate_email(email)
    code = str(code or "").strip()
    if not code:
        raise ValueError("Doğrulama kodunu girin.")
    user = _find_user_by_email(connection, email)
    if not user:
        raise ValueError("Bu e-posta adresi ile eşleşen kullanıcı bulunamadı.")
    consume_rate_limit(
        connection,
        "verification_verify",
        email,
        VERIFICATION_VERIFY_RATE_LIMIT[0],
        VERIFICATION_VERIFY_RATE_LIMIT[1],
        "Çok fazla doğrulama denemesi yapıldı.",
    )
    cursor = connection.cursor(dictionary=True)
    cursor.execute(
        """
        SELECT id
        FROM email_verification_tokens
        WHERE user_id = %s AND token_hash = %s AND used_at IS NULL AND expires_at >= NOW()
        ORDER BY created_at DESC
        LIMIT 1
        """,
        (user["id"], _token_hash(code)),
    )
    row = cursor.fetchone()
    if not row:
        raise ValueError("Doğrulama kodu geçersiz veya süresi dolmuş.")
    cursor.execute(
        "UPDATE kullanicilar SET email_verified = TRUE, is_active = TRUE, email_verified_at = NOW() WHERE id = %s",
        (user["id"],),
    )
    cursor.execute("UPDATE email_verification_tokens SET used_at = NOW() WHERE id = %s", (row["id"],))
    connection.commit()
    return {"status": "verified", "message": "E-posta adresi doğrulandı. Hesap aktif hale getirildi."}


def send_password_reset_code(connection: Any, identifier: str) -> dict[str, str]:
    ensure_account_security_schema(connection)
    identifier = str(identifier or "").strip()
    if not identifier:
        raise ValueError("Kullanıcı adı veya e-posta girin.")
    user = _find_user_by_identifier(connection, identifier)
    if not user:
        raise ValueError("Bu bilgi ile eşleşen kullanıcı bulunamadı.")
    if not user.get("email"):
        raise ValueError("Bu kullanıcı için e-posta adresi tanımlı değil.")
    consume_rate_limit(
        connection,
        "password_reset_send",
        identifier,
        PASSWORD_RESET_SEND_RATE_LIMIT[0],
        PASSWORD_RESET_SEND_RATE_LIMIT[1],
        "Çok fazla şifre sıfırlama kodu talebi alındı.",
    )
    code = _create_numeric_code()
    expires_at = datetime.now() + timedelta(minutes=PASSWORD_RESET_EXPIRY_MINUTES)
    cursor = connection.cursor()
    cursor.execute("UPDATE password_reset_tokens SET used_at = NOW() WHERE user_id = %s AND used_at IS NULL", (user["id"],))
    cursor.execute(
        "INSERT INTO password_reset_tokens (user_id, reset_code_hash, expires_at) VALUES (%s, %s, %s)",
        (user["id"], _token_hash(code), expires_at),
    )
    connection.commit()
    subject, body, html_body = _password_reset_email_content(str(user["kullanici_adi"]), code)
    return _send_email(str(user["email"]), subject, body, html_body)


def reset_password_with_code(connection: Any, identifier: str, code: str, new_password: str) -> dict[str, str]:
    ensure_account_security_schema(connection)
    identifier = str(identifier or "").strip()
    code = str(code or "").strip()
    if not identifier:
        raise ValueError("Kullanıcı adı veya e-posta girin.")
    if not code:
        raise ValueError("Sıfırlama kodunu girin.")
    validate_password_policy(new_password)
    user = _find_user_by_identifier(connection, identifier)
    if not user:
        raise ValueError("Bu bilgi ile eşleşen kullanıcı bulunamadı.")
    consume_rate_limit(
        connection,
        "password_reset_verify",
        identifier,
        PASSWORD_RESET_VERIFY_RATE_LIMIT[0],
        PASSWORD_RESET_VERIFY_RATE_LIMIT[1],
        "Çok fazla şifre sıfırlama denemesi yapıldı.",
    )
    cursor = connection.cursor(dictionary=True)
    cursor.execute(
        """
        SELECT id
        FROM password_reset_tokens
        WHERE user_id = %s AND reset_code_hash = %s AND used_at IS NULL AND expires_at >= NOW()
        ORDER BY created_at DESC
        LIMIT 1
        """,
        (user["id"], _token_hash(code)),
    )
    row = cursor.fetchone()
    if not row:
        raise ValueError("Sıfırlama kodu geçersiz veya süresi dolmuş.")
    cursor.execute("UPDATE kullanicilar SET sifre_hash = %s WHERE id = %s", (hash_password(new_password), user["id"]))
    cursor.execute("UPDATE password_reset_tokens SET used_at = NOW() WHERE id = %s", (row["id"],))
    connection.commit()
    return {"status": "reset", "message": "Şifreniz başarıyla güncellendi."}
